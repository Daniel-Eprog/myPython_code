
x = int(input("What is the value of x?"))
y = int(input("What is the value of y?"))
z = int(input("What is the value of z?"))

d = int(input("How would you like to run your numbers? 1) add 2) subtract 3) divide 4) multiply" ))

if d == 1:
    f = x + y + z
    print(f)
if d == 2:
    f = x - y - z
    print(f)
if d == 3:
    f = x / y / z
    print(f)
if d == 4:
    f = x * y * z
    print (f)
